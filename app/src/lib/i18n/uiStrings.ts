export function translate(lang: UiLang, key: string, vars?: Record<string, string | number>): string {
  let s = UI_STRINGS[lang][key] ?? UI_STRINGS.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replaceAll(`{${k}}`, String(v));
    }
  }
  return s;
}

